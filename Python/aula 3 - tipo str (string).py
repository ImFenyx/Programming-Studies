# Python é uma linguagem de tipagem dinamica forte
# Todas as linguagens de programaçao tem característica de tipagem
# Na tipagem dinâmica significa que o Python não precisa especificar tipos para saber qual é

# Exemplo:
print("1234") # O Python automaticamente detectou como string (str) sem especificar como: print (str(1234))
# Aqui vai mostrar o tipo que o python escolheu:
type("1234")

# String é texto/cadeia de caracteres, geralmente especificado pelas aspas